// Client API pour communiquer avec le backend Node.js

const API_BASE_URL = process.env.NEXT_PUBLIC_BACKEND_URL || 'http://localhost:3003';

class ApiClient {
  constructor() {
    this.baseUrl = API_BASE_URL;
    this.token = null;
    console.log('API Base URL:', this.baseUrl);
  }

  setToken(token) {
    this.token = token;
    if (typeof window !== 'undefined') {
      // Stocker dans localStorage pour le client
      localStorage.setItem('authToken', token);
      // Stocker dans un cookie pour le middleware
      document.cookie = `authToken=${token}; path=/; max-age=86400; samesite=lax`;
    }
  }

  getToken() {
    if (typeof window !== 'undefined') {
      // Essayer de r√©cup√©rer du cookie d'abord, puis de localStorage
      const cookieValue = document.cookie
        .split('; ')
        .find(row => row.startsWith('authToken='))
        ?.split('=')[1];
      
      return cookieValue || localStorage.getItem('authToken');
    }
    return null;
  }

  clearToken() {
    this.token = null;
    if (typeof window !== 'undefined') {
      localStorage.removeItem('authToken');
      document.cookie = 'authToken=; path=/; expires=Thu, 01 Jan 1970 00:00:00 GMT';
    }
  }

  async request(endpoint, options = {}) {
    let url = `${this.baseUrl}${endpoint}`;
    const token = this.token || this.getToken();
    
    // Ajouter un timestamp pour √©viter le cache uniquement pour les requ√™tes GET
    if (options.method === undefined || options.method === 'GET') {
      const separator = endpoint.includes('?') ? '&' : '?';
      url += `${separator}_t=${Date.now()}`;
    }
    
    const config = {
      headers: {
        'Content-Type': 'application/json',
        ...options.headers,
      },
      ...options,
    };

    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }

    try {
      // V√©rifier si nous sommes dans un environnement navigateur
      if (typeof window === 'undefined') {
        throw new Error('API Client ne peut √™tre utilis√© que c√¥t√© client (navigateur)');
      }

      console.log('üîç API Request URL:', url);
      console.log('üîç API Request Config:', JSON.stringify(config, null, 2));
      console.log('üîç Token pr√©sent:', !!token);
      console.log('üîç Token value:', token ? token.substring(0, 20) + '...' : 'null');
      
      console.log('üîç Starting fetch request...');
      
      // Configuration simplifi√©e sans timeout pour √©viter les AbortError
      const response = await fetch(url, config);
      
      console.log('‚úÖ API Response status:', response.status, response.statusText);
      console.log('‚úÖ API Response headers:', Object.fromEntries(response.headers.entries()));
      console.log('‚úÖ API Response URL:', response.url); // Ajouter l'URL finale apr√®s redirections
      
      if (!response.ok) {
        let errorData = {};
        try {
          errorData = await response.json();
        } catch (jsonError) {
          console.error('‚ùå Failed to parse error response:', jsonError);
        }
        
        // Log d√©taill√© pour comprendre la structure de l'erreur
        console.log('üîç Error response status:', response.status);
        console.log('üîç Error response headers:', Object.fromEntries(response.headers.entries()));
        console.log('üîç Error data structure:', errorData);
        console.log('üîç Error data type:', typeof errorData);
        
        const errorMessage = errorData.message || errorData.error || `HTTP error! status: ${response.status}`;
        console.error('‚ùå API Error:', errorMessage, errorData);
        throw new Error(errorMessage);
      }

      const responseText = await response.text();
      console.log('üìÑ API Response text:', responseText);
      
      let responseData;
      try {
        responseData = JSON.parse(responseText);
        console.log('üéâ API Success:', responseData);
      } catch (parseError) {
        console.error('‚ùå Erreur de parsing JSON:', parseError);
        console.error('‚ùå Contenu qui a √©chou√©:', responseText);
        throw new Error('R√©ponse JSON invalide du serveur');
      }
      
      return responseData;
    } catch (error) {
      if (error.name === 'AbortError') {
        console.error('‚è∞ API Request timeout:', error);
        throw new Error('La requ√™te a expir√© (timeout)');
      }
      console.error('üí• API Request failed:', error);
      console.error('üí• Error details:', error.stack);
      console.error('üí• Error name:', error.name);
      console.error('üí• Error message:', error.message);
      throw error;
    }
  }

  // Authentication
  async login(email, password) {
    const response = await this.request('/api/auth/login', {
      method: 'POST',
      body: JSON.stringify({ email, password }),
    });

    if (response.success && response.token) {
      this.setToken(response.token);
    }

    return response;
  }

  async logout() {
    this.clearToken();
    return { success: true, message: 'D√©connexion r√©ussie' };
  }

  // Users
  async getUsers() {
    return this.request('/api/users');
  }

  // Diligences
  async getDiligences() {
    return this.request('/api/diligences');
  }

  async getDiligence(id) {
    return this.request(`/api/diligences/${id}`);
  }

  async createDiligence(diligenceData) {
    return this.request('/api/diligences', {
      method: 'POST',
      body: JSON.stringify(diligenceData),
    });
  }

  async updateDiligence(id, diligenceData) {
    return this.request(`/api/diligences/${id}`, {
      method: 'PUT',
      body: JSON.stringify(diligenceData),
    });
  }

  async deleteDiligence(id) {
    return this.request(`/api/diligences/${id}`, {
      method: 'DELETE',
    });
  }

  async markDiligenceAsViewed(diligenceId, userId) {
    return this.request(`/api/diligences/${diligenceId}/mark-viewed`, {
      method: 'POST',
      body: JSON.stringify({ userId }),
    });
  }

  // Health check
  async healthCheck() {
    return this.request('/api/health');
  }

  // V√©rifier si l'utilisateur est connect√©
  isAuthenticated() {
    return !!this.getToken();
  }

  // R√©cup√©rer les informations de l'utilisateur connect√©
  async getCurrentUser() {
    return this.request('/api/auth/me');
  }

  // Mettre √† jour le profil utilisateur
  async updateProfile(profileData) {
    return this.request('/api/auth/profile', {
      method: 'PUT',
      body: JSON.stringify(profileData),
    });
  }

  // Traiter une diligence avec upload de fichiers
  async traiterDiligence(diligenceId, formData) {
    const url = `${this.baseUrl}/api/diligences/${diligenceId}/traitement`;
    const token = this.token || this.getToken();
    
    const config = {
      method: 'POST',
      headers: {},
      body: formData,
    };

    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }

    try {
      console.log('üîç Traitement diligence URL:', url);
      const response = await fetch(url, config);
      
      if (!response.ok) {
        let errorData = {};
        try {
          errorData = await response.json();
        } catch (jsonError) {
          console.error('‚ùå Failed to parse error response:', jsonError);
        }
        const errorMessage = errorData.error || `HTTP error! status: ${response.status}`;
        console.error('‚ùå Traitement diligence error:', errorMessage, errorData);
        throw new Error(errorMessage);
      }

      const responseData = await response.json();
      console.log('üéâ Traitement diligence success:', responseData);
      return responseData;
    } catch (error) {
      console.error('üí• Traitement diligence failed:', error);
      throw error;
    }
  }

  // Valider ou rejeter une diligence
  async validateDiligence(diligenceId, validationStatus, comment = '') {
    return this.request(`/api/diligences/${diligenceId}/validate`, {
      method: 'POST',
      body: JSON.stringify({ validation_status: validationStatus, comment }),
    });
  }

  // R√©cup√©rer les donn√©es de traitement d'une diligence
  async getDiligenceTraitements(diligenceId) {
    return this.request(`/api/diligences/${diligenceId}/traitements`);
  }

  // R√©cup√©rer les diligences archiv√©es via l'API route Next.js
  async getArchivedDiligences() {
    console.log('üîç Appel de getArchivedDiligences() via API route Next.js');
    try {
      const token = this.token || this.getToken();
      const headers = {
        'Content-Type': 'application/json',
      };

      if (token) {
        headers.Authorization = `Bearer ${token}`;
      }

      // Utiliser fetch directement pour appeler l'API route Next.js (m√™me domaine)
      const response = await fetch('/api/diligences/archives', {
        headers,
      });

      if (!response.ok) {
        const errorText = await response.text();
        console.error('‚ùå Erreur API route:', response.status, errorText);
        throw new Error(`Erreur ${response.status}: ${errorText}`);
      }

      const result = await response.json();
      console.log('‚úÖ R√©ponse de getArchivedDiligences:', result);
      return result;
    } catch (error) {
      console.error('‚ùå Erreur dans getArchivedDiligences:', error);
      console.error('‚ùå Stack:', error.stack);
      throw error;
    }
  }

  // Archiver manuellement une diligence via l'API route Next.js
  async archiveDiligence(diligenceId) {
    console.log('üîç Appel de archiveDiligence() via API route Next.js');
    try {
      const token = this.token || this.getToken();
      const headers = {
        'Content-Type': 'application/json',
      };

      if (token) {
        headers.Authorization = `Bearer ${token}`;
      }

      // Utiliser fetch directement pour appeler l'API route Next.js (m√™me domaine)
      const response = await fetch(`/api/diligences/${diligenceId}/archive`, {
        method: 'POST',
        headers,
      });

      if (!response.ok) {
        let errorData = {};
        try {
          errorData = await response.json();
        } catch (jsonError) {
          console.error('‚ùå Failed to parse error response:', jsonError);
          const errorText = await response.text();
          throw new Error(`Erreur ${response.status}: ${errorText}`);
        }
        
        console.error('‚ùå Erreur API route d\'archivage:', response.status, errorData);
        
        // Extraire le message d'erreur de la r√©ponse JSON
        const errorMessage = errorData.error || errorData.message || `Erreur ${response.status}`;
        throw new Error(errorMessage);
      }

      const result = await response.json();
      console.log('‚úÖ R√©ponse de archiveDiligence:', result);
      return result;
    } catch (error) {
      console.error('‚ùå Erreur dans archiveDiligence:', error);
      console.error('‚ùå Stack:', error.stack);
      throw error;
    }
  }

  // R√©cup√©rer tous les fichiers d'une diligence (pi√®ces jointes + fichiers des traitements)
  async getDiligenceFiles(diligenceId) {
    return this.request(`/api/diligences/${diligenceId}/files`);
  }
}

// Instance singleton
export const apiClient = new ApiClient();

// Hook pour utiliser le client API (√† utiliser dans les composants React)
export const useApi = () => {
  return apiClient;
};

export default apiClient;